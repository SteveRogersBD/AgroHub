    )
    
    /**
     * Get status icon based on health percentage
     * @param healthPercentage Health percentage (0-100)
     * @return Appropriate status icon with color
     */
    fun getHealthStatusIcon(healthPercentage: Float): StatusIcon {
        return when {
            healthPercentage >= 70f -> HealthyStatus
            healthPercentage >= 40f -> WarningStatus
            else -> CriticalStatus
        }
    }
    
    /**
     * Get status icon based on risk level
     * @param riskLevel Risk level string ("LOW", "MEDIUM", "HIGH")
     * @return Appropriate status icon with color
     */
    fun getRiskStatusIcon(riskLevel: String): StatusIcon {
        return when (riskLevel.uppercase()) {
            "LOW" -> HealthyStatus
            "MEDIUM" -> WarningStatus
            "HIGH" -> CriticalStatus
            else -> InfoStatus
        }
    }
    
