    // ========== Status Icons with Color Coding ==========
    
    /**
     * Status icon data class containing icon and color
     */
    data class StatusIcon(
        val icon: ImageVector,
        val color: Color,
        val description: String
    )
    
    /**
     * Healthy status icon - green color for good/healthy status
     * Requirements: 12.5
     */
    val HealthyStatus = StatusIcon(
        icon = Icons.Filled.CheckCircle,
